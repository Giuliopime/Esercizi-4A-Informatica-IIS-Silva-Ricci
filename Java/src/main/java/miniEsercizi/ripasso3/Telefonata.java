package miniEsercizi.ripasso3;

public class Telefonata {
    private int numero, durata;

    public Telefonata(int numero, int durata) {
        this.numero = numero;
        this.durata = durata;
    }

    public int getNumero() {
        return numero;
    }

    public int getDurata() {
        return durata;
    }
}
